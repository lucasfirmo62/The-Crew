function daySelect(dayAPI){
    if(dayAPI === "seg"){
        return "segunda-feira";
    }else if(dayAPI === "ter"){
        return "terça-feira";
    }else if(dayAPI === "qua"){
        return "quarta-feira";
    }else if(dayAPI === "qui"){
        return "quinta-feira";
    }else if(dayAPI === "sex"){
        return "sexta-feira";
    }else if(dayAPI === "sab"){
        return "sábado";
    }
}